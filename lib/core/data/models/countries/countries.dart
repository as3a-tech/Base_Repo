class CountryCodeModel extends Equatable {
  String? nameEn;
  String? nameAr;
  String? nameTr;
  String? shortName;
  String? countryCode;
  String? flagSVG;
  List<String>? startDigits;
  int? numLenFrom;
  int? numLenTo;

  CountryCodeModel({
    this.nameEn,
    this.nameAr,
    this.nameTr,
    this.shortName,
    this.countryCode,
    this.flagSVG,
    this.startDigits,
    this.numLenFrom,
    this.numLenTo,
  });

  CountryCodeModel.fromJson(Map<String, dynamic> json) {
    nameEn = json['name_en'];
    nameAr = json['name_ar'];
    nameTr = json['name_tr'];
    shortName = json['shortName'];
    countryCode = json['countryCode'];
    flagSVG =
        'assets/countries/${json['shortName'].toString().toLowerCase()}.png';
    startDigits = json['startDigits']?.cast<String>();
    numLenFrom = json['numLenFrom'];
    numLenTo = json['numLenTo'];
  }

  Widget buildFlagWidget({double size = 30}) => SizedBox(
    width: size,
    height: size,
    child: Center(
      child: CustomImage(
        image: flagSVG ?? "",
        width: size,
        height: size,
        fit: BoxFit.contain,
        errorWidget: SizedBox(width: size, height: size),
      ),
    ),
  );

  @override
  List<Object?> get props => [shortName, countryCode];
}

